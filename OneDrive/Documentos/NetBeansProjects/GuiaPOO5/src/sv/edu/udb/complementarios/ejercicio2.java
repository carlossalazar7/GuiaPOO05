        int n2 = Integer.parseInt(txt2.getText());
        String prueba = (String) this.op.getSelectedItem();
        int result;
        String show = null;

        switch (prueba) {
            case "suma":
                try {

                } catch (Exception e) {
                    System.out.println(e);
                    txtresult.setText("");
                }
                result = n1 + n2;
                show = "resultado: " + result;
                break;
            case "resta":
                try {
                    result = n1 - n2;
                    show = "resultado: " + result;

                } catch (Exception e) {
                    System.out.println(e);
                }
                break;
            case "multiplicacion":
                try {
                    result = n1 * n2;
                    show = "resultado: " + result;

                } catch (Exception e) {
                    System.out.println(e);
                }
                break;
            case "division":
                try {
                    result = n1 / n2;
                    show = "resultado: " + result;

                } catch (Exception e) {
                    System.out.println(e);
                    txtresult.setText("No se puede dividor entre 0");
                }
                break;
        }

        txtresult.setText(show);
