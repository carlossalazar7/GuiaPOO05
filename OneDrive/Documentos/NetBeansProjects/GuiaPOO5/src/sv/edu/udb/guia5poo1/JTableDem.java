    private void t1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t1MouseClicked
        int fila = t1.rowAtPoint(evt.getPoint());
        int columna = t1.columnAtPoint(evt.getPoint());

        if (fila > -1 && columna > -1) {
            JOptionPane.showMessageDialog(this, "Valor Seleccionado: " + dtm.getValueAt(fila, columna) + "\n");
        }
    }//GEN-LAST:event_t1MouseClicked

