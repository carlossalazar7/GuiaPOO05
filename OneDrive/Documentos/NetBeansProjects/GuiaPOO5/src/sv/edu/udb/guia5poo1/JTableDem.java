    private void t1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t1MousePressed
        int fila = t1.rowAtPoint(evt.getPoint());
        int columna = t1.columnAtPoint(evt.getPoint());
        try {
            if (fila > -1 && columna > -1) {
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    name.setText((String) dtm.getValueAt(columna, 0));
                    lastName.setText((String) dtm.getValueAt(columna, 1));
                    hobby.setText((String) dtm.getValueAt(columna, 2));
                    experiece.setText((String) dtm.getValueAt(columna, 3).toString());
                    status.setText((String) dtm.getValueAt(columna, 4).toString());
                }
            }
        } catch (NumberFormatException e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_t1MousePressed

